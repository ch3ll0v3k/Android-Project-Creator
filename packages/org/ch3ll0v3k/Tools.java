public class Tools {

    // ==============================================================================
    //public Tools() { }

    // ==============================================================================
    public static int dp2px(int dp, float screen_dens) {
        return (int) (dp * screen_dens + 0.5f);
    }

    public static int px2dp(int px, float screen_dens) {
        return (int) (px / screen_dens + 0.5f);
    }

    // ================================================================
    public static long getUnixStampWithOffset(int offset) {

        // -------------------------------------------------------
        return (System.currentTimeMillis()/1000) - offset; // 3582
        // -------------------------------------------------------

    }

    // ==============================================================================

}
